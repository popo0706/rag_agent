# =============================================================================
# 【概要】
# このスクリプトは Python 標準モジュールの operator.itemgetter を使い、
# 辞書オブジェクトから "topic" というキーに対応する値を取り出して表示します。
# =============================================================================

from operator import itemgetter  # 辞書やタプルから要素を取り出すユーティリティ

topic_getter = itemgetter("topic")  # "topic" 専用の取り出し関数を作成
topic = topic_getter(
    {  # 作った関数に辞書を渡して値を取得
        "topic": "生成AIの今後について"  # ここが取り出される対象
    }
)
print(topic)  # -> 生成AIの今後について
